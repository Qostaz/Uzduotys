package lt.bt.Testas.objektai_temperatura;

public class Matavimai {
	private int monthInt;
	private int tempTimes;
	private double allTempAverage;
	
	public int getMonthInt() {
		return monthInt;
	}
	public void setMonthInt(int monthInt) {
		this.monthInt = monthInt;
	}
	public int getTempTimes() {
		return tempTimes;
	}
	public void setTempTimes(int tempTimes) {
		this.tempTimes = tempTimes;
	}
	public double getAllAverage() {
		return allTempAverage;
	}
	public void setAllAverage(double allAverage) {
		this.allTempAverage = allAverage;
	}
}
