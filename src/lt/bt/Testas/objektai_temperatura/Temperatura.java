package lt.bt.Testas.objektai_temperatura;

public class Temperatura {	
	private double[] tempArray;
	private String monthName;
	private double tempAverage;
	
	public double[] getTempArray() {
		return tempArray;
	}
	public void setTempArray(double[] tempArray) {
		this.tempArray = tempArray;
	}
	public String getMonthName() {
		return monthName;
	}
	public void setMonthName(String monthName) {
		this.monthName = monthName;
	}
	public double getTempAverage() {
		return tempAverage;
	}
	public void setTempAverage(double tempAverage) {
		this.tempAverage = tempAverage;
	}
	
}
